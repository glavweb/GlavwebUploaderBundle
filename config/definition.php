<?php

use Symfony\Component\Config\Definition\Configurator\DefinitionConfigurator;

return static function (DefinitionConfigurator $definition): void {
    $definition->rootNode()
        ->children()
            ->arrayNode('orphanage')
                ->addDefaultsIfNotSet()
                ->children()
                    ->scalarNode('lifetime')->defaultValue(604800)->end()
                ->end()
            ->end()
            ->scalarNode('model_manager')->defaultValue('glavweb_uploader.model_manager.orm')->end()
            ->scalarNode('base_url')->defaultValue('')->end()
            ->scalarNode('storage')->defaultValue('glavweb_uploader.storage.filesystem')->end()
            ->scalarNode('temp_directory')->defaultValue('%kernel.project_dir%/var/temp')
                ->cannotBeEmpty()->end()
            ->arrayNode('chunk_upload')
                ->addDefaultsIfNotSet()
                ->children()
                    ->scalarNode('file_id_request_parameter')->defaultValue('dzuuid')->end()
                    ->scalarNode('total_count_request_parameter')->defaultValue('dztotalchunkcount')->end()
                    ->scalarNode('current_index_request_parameter')->defaultValue('dzchunkindex')->end()
                ->end()
            ->end()
            ->arrayNode('mappings_defaults')
                ->addDefaultsIfNotSet()
                ->children()
                    ->arrayNode('providers')
                        ->prototype('scalar')->end()
                    ->end()
                    ->scalarNode('route_prefix')->defaultValue('')->end()
                    ->arrayNode('allowed_mimetypes')
                        ->prototype('scalar')->end()
                    ->end()
                    ->arrayNode('disallowed_mimetypes')
                        ->prototype('scalar')->end()
                    ->end()
                    ->scalarNode('max_size')
                        ->defaultValue(\PHP_INT_MAX)
                        ->info('Maximum file size in bytes. Set max_size to -1 for gracefully downgrade this number to the systems max upload size.')
                    ->end()
                    ->scalarNode('max_files')
                        ->defaultValue(\PHP_INT_MAX)
                        ->info('Set max files.')
                    ->end()
                    ->arrayNode('width')
                        ->info('Image width in pixels.')
                        ->beforeNormalization()
                            ->ifTrue(static fn ($v): bool => is_int($v))
                            ->then(static fn ($v): array => ['min' => $v, 'max' => $v])
                        ->end()
                        ->addDefaultsIfNotSet()
                        ->children()
                            ->integerNode('min')
                                ->defaultNull()
                            ->end()
                            ->integerNode('max')
                                ->defaultNull()
                            ->end()
                        ->end()
                    ->end()
                    ->arrayNode('height')
                        ->info('Image height in pixels.')
                        ->beforeNormalization()
                            ->ifTrue(static fn ($v): bool => is_int($v))
                            ->then(static fn ($v): array => ['min' => $v, 'max' => $v])
                        ->end()
                        ->addDefaultsIfNotSet()
                        ->children()
                            ->integerNode('min')
                                ->defaultNull()
                            ->end()
                            ->integerNode('max')
                                ->defaultNull()
                            ->end()
                        ->end()
                    ->end()
                    ->booleanNode('use_orphanage')->defaultFalse()->end()
                    ->scalarNode('upload_directory')->end()
                    ->scalarNode('upload_directory_url')->end()
                    ->scalarNode('namer')->defaultValue('glavweb_uploader.namer.uniqid')->end()
                    ->booleanNode('description_enabled')
                        ->info('Enables description field of media file model.')
                        ->defaultTrue()
                    ->end()
                ->end()
            ->end()
            ->arrayNode('mappings')
                ->useAttributeAsKey('id')
                ->prototype('array')
                    ->children()
                        ->arrayNode('providers')
                            ->prototype('scalar')->end()
                        ->end()
                        ->scalarNode('route_prefix')->end()
                        ->arrayNode('allowed_mimetypes')
                            ->prototype('scalar')->end()
                        ->end()
                        ->arrayNode('disallowed_mimetypes')
                            ->prototype('scalar')->end()
                        ->end()
                        ->scalarNode('max_size')
                            ->info('Maximum file size in bytes. Set max_size to -1 for gracefully downgrade this number to the systems max upload size.')
                        ->end()
                        ->scalarNode('max_files')
                            ->info('Set max files.')
                        ->end()
                        ->arrayNode('width')
                            ->info('Image width in pixels.')
                            ->beforeNormalization()
                                ->ifTrue(static fn ($v): bool => is_int($v))
                                ->then(static fn ($v): array => ['min' => $v, 'max' => $v])
                            ->end()
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->integerNode('min')
                                    ->defaultNull()
                                ->end()
                                ->integerNode('max')
                                    ->defaultNull()
                                ->end()
                            ->end()
                        ->end()
                        ->arrayNode('height')
                            ->info('Image height in pixels.')
                            ->beforeNormalization()
                                ->ifTrue(static fn ($v): bool => is_int($v))
                                ->then(static fn ($v): array => ['min' => $v, 'max' => $v])
                            ->end()
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->integerNode('min')
                                    ->defaultNull()
                                ->end()
                                ->integerNode('max')
                                    ->defaultNull()
                                ->end()
                            ->end()
                        ->end()
                        ->booleanNode('use_orphanage')->end()
                        ->scalarNode('upload_directory')->end()
                        ->scalarNode('upload_directory_url')->end()
                        ->scalarNode('namer')->end()
                        ->booleanNode('description_enabled')
                            ->info('Enables description field of media file model.')
                            ->defaultTrue()
                        ->end()
                    ->end()
                ->end()
            ->end()
        ->end()
    ;
};
